package section7.composition;

public class CeilingCompositionChallenge {
    private int height;
    private int paintedColor;

    public CeilingCompositionChallenge(int height, int paintedColor) {
        this.height = height;
        this.paintedColor = paintedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
